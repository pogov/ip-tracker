import React, { useState, useEffect } from "react";
import PropTypes from "prop-types";
import "../index.css";
import Search from "./Search";

const Header = ({ query, children, setLoading }) => {
  const [value, setValue] = useState();
  const [error, setError] = useState(false);

  const regex = /((^\s*((([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5]))\s*$)|(^\s*((([0-9A-Fa-f]{1,4}:){7}([0-9A-Fa-f]{1,4}|:))|(([0-9A-Fa-f]{1,4}:){6}(:[0-9A-Fa-f]{1,4}|((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){5}(((:[0-9A-Fa-f]{1,4}){1,2})|:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){4}(((:[0-9A-Fa-f]{1,4}){1,3})|((:[0-9A-Fa-f]{1,4})?:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){3}(((:[0-9A-Fa-f]{1,4}){1,4})|((:[0-9A-Fa-f]{1,4}){0,2}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){2}(((:[0-9A-Fa-f]{1,4}){1,5})|((:[0-9A-Fa-f]{1,4}){0,3}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){1}(((:[0-9A-Fa-f]{1,4}){1,6})|((:[0-9A-Fa-f]{1,4}){0,4}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(:(((:[0-9A-Fa-f]{1,4}){1,7})|((:[0-9A-Fa-f]{1,4}){0,5}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:)))(%.+)?\s*$))/gi;

  const handleError = (e) => {
    setError(true);
    setTimeout(() => {
      setError(false);
    }, 2000);
    e.target[0].value = "";
  };

  const handleSearch = (e) => {
    e.preventDefault();
    if (!regex.test(e.target[0].value)) {
      handleError(e);
      return;
    }
    if (e.target[0].value === value) {
      e.target[0].value = "";
      return;
    }
    setValue(e.target[0].value);
    e.target[0].value = "";
    setLoading(true);
  };

  useEffect(() => {
    query(value);
  }, [value, query]);

  return (
    <div className="bg">
      <div className="header">
        <h2>IP Adress Tracker</h2>
        <Search search={handleSearch} />
        {error ? (
          <p className="info-alert red">This is not IP4 or IP6 format</p>
        ) : null}
      </div>
      {children}
    </div>
  );
};

Header.propTypes = {
  query: PropTypes.func.isRequired,
  children: PropTypes.object,
  setLoading: PropTypes.func,
};

export default Header;
